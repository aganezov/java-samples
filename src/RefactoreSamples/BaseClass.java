/* Super Class has been changed via    Ctrl + T -> Extract Superclass  */
public class BaseClass extends ExtractedSuperClass {
    private int field2;
    AnotherClass anotherClass;

    /* Parameter extracted by Ctrl + T -> Extract Parameter */
    public void greet(String greeting){
        /* Variable extracted by Ctrl + T -> Extract Variable */
        StringBuilder stringBuilder = new StringBuilder();
        /* Variable extracted by Ctrl + T -> Extract Variable */

        /* Method invocation generated by Ctrl + T -> Extract Method */
        /* Parameter added by Ctrl + T (in appendName method) -> Extract parameter */
        appendName(stringBuilder, "My name");
        System.out.println(greeting);
    }
    /* Generated by Ctrl + T -> Extract Method */
    /* Parameter extracted by Ctrl + T -> Extract parameter */
    private void appendName(StringBuilder stringBuilder, String name) {
        /* Variable was inlined by    Ctrl + T -> Inline Variable */
        stringBuilder.append(name.toLowerCase());
    }

    /* Generated by Ctrl + T -> Extract Field */
    private int randomIntValue;

    public void printRandomIntValue(){
        /* Generated by Ctrl + T -> Extract Field */
        randomIntValue = anotherClass.randomIntValue();

        System.out.println(randomIntValue);
    }
    /* Generate by Ctrl + T -> Encapsulate Fields */
    public int getField1() {
        return field1;
    }

    /* Generate by Ctrl + T -> Encapsulate Fields */
    public void setField1(int field1) {
        this.field1 = field1;
    }

    /* Generate by Ctrl + T -> Encapsulate Fields */
    public int getField2() {
        return field2;
    }

    /* Generate by Ctrl + T -> Encapsulate Fields */
    public void setField2(int field2) {
        this.field2 = field2;
    }
}
